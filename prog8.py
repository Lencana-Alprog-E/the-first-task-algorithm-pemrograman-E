# penjumlahan 
x = 24
y = 89
hasil = x + y
print ("nilai dari x + y = ", hasil) 

# pengurangan
hasil = x - y
print ("nilai dari x - y = ", hasil) 

# pembagian
hasil = x / y
print ("nilai dari x / y = ", hasil) 

# perkalian
hasil = x * y
print ("nilai dari x * y = ", hasil) 

# eksponen ( perpangkatan)
hasil = x ** y
print ("nilai dari x ** y = ", hasil) 

# modulus ( sisa bagi)
hasil = x // y
print ("nilai dari x // y = ", hasil) 

# Floor division
hasil = x // y
print ("nilai dari x FD y = ", hasil) 

